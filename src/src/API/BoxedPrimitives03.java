package API;

public class BoxedPrimitives03 {
	public static void main(String[] args) {
		System.out.println(Integer.toBinaryString(110));
		System.out.println(Integer.toOctalString(110));
		System.out.println(Integer.toHexString(110));

		System.out.println(Integer.toString(1000,36)); //1000을 36진수로, 36진수가 끝임.
		
		
	}

}
